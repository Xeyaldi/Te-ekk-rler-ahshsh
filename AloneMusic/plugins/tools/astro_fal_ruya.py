@app.on_message(filters.command("burc"))
async def burc(client, message: Message):

    if len(message.command) < 2:
        await message.reply("Bürcünü yaz.\nNümunə:\n`/burc şir`")
        return

    burc_adı = message.command[1].lower()

    if burc_adı in burclar:
        await message.reply(f"♈ **{burc_adı.capitalize()} Bürcü**\n\n{burclar[burc_adı]}")
    else:
        await message.reply("Düzgün bir bürc adı yaz.")

